#for loops
for i in range(10):
    print(i)
print("------------------")
for i in range(1, 11):
    print(i)
print("------------------")
for i in range(1, 11, 2):
    print(i)
print("------------------")
for i in range(10, 0, -1):
    print(i)
print("------------------")
for i in range(10, 0, -2):
    print(i)
print("------------------")
for i in range(10, 0, -2):
    print(i, end=" ")
print("------------------")
for i in range(10, 0, -2):
    print(i, end=" ")
print("------------------")
#while loops
i = 0
while i < 10:
    print(i)
    i += 1
print("------------------")
i = 1
while i <= 10:
    print(i)
    i += 1
print("------------------")
